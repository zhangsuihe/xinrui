package xr.com.pojo;

public class Food {
    private String title;
    private String pid;
    private String level;

    public void setTitle(String title) {
        this.title = title;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public void setLevel(String level) {
        this.level = level;
    }

    public String getTitle() {

        return title;
    }

    public String getPid() {
        return pid;
    }

    public String getLevel() {
        return level;
    }
}
